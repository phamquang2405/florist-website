function localizeEnglishProduct<T extends { category: string; price: string; tag?: string }>(product: T): T {
  return {
    ...product,
    category: englishCategoryMap[product.category] ?? product.category,
    price: product.price === 'Liên hệ' ? 'Contact' : product.price,
    tag: product.tag ? englishTagMap[product.tag] ?? product.tag : product.tag,
  };
}

function localizeEnglishCollection<
  T extends {
    category: string;
    products: Array<{ category: string; price: string; tag?: string }>;
  },
>(collection: T): T {
  return {
    ...collection,
    category: englishCategoryMap[collection.category] ?? collection.category,
    products: collection.products.map((product) => localizeEnglishProduct(product)),
  };
}

